        SUBROUTINE BRINV(A,N)
	    INCLUDE 'ABA_PARAM.INC'
         
        REAL(8),DIMENSION(N,N):: A
        INTEGER,DIMENSION(N)::IS,JS
        INTEGER ::I,J,L,K,N
		REAL(8) :: D,T
		
		INTEGER,DIMENSION(10) :: INTV
		REAL(8),DIMENSION(10) :: REALV
		CHARACTER*80,DIMENSION(10) :: CHARV
        
		REAL(8),DIMENSION(N) :: TMPVEC

		L=1
		DO K=1,N
			D=0.0D0
			DO I=K,N
				DO J=K,N
				
				   !WRITE(*,*) I,J,ABS(A(I,J))
				   
				   IF(ABS(A(I,J))>D) THEN
				   
					   D=ABS(A(I,J))
					   IS(K)=I
                       JS(K)=J   
					   
				   END IF
				   
				   !WRITE(*,*) D 
				   
			    END DO
		    END DO

		   IF((D+1.0_8)==1.0_8) THEN
			   L=0_8
			   !WRITE(*,'(1X,A)')
			   CALL STDB_ABQERR(-3,'ERROR IN BRINV**NOT INV',INTV,REALV,CHARV)
		   END IF

		   TMPVEC(:) = A(K,:)
		   A(K,:) = A(IS(K),:)
		   A(IS(K),:) = TMPVEC(:)
		   
		   TMPVEC(:) = A(:,K)
		   A(:,K) = A(:,JS(K))
		   A(:,JS(K)) = TMPVEC(:)
		   
	       A(K,K)=1.0D0/A(K,K)

		  DO J=1,N
			IF (J/=K) THEN
				A(K,J)=A(K,J)*A(K,K)
			END IF
		  END DO

		  DO I=1,N
			  IF(I/=K) THEN
				  DO J=1,N
					  IF(J/=K) THEN
						  A(I,J)=A(I,J)-A(I,K)*A(K,J)
					  END IF
				  END DO
			  END IF
		  END DO

		  DO I=1,N
			  IF(I/=K) THEN
				  A(I,K)=-A(I,K)*A(K,K)
			  END IF
		  END DO

		END DO
		
		DO K=N,1,-1
			TMPVEC(:) = A(K,:)
		    A(K,:) = A(JS(K),:)
		    A(JS(K),:) = TMPVEC(:)
		   
		    TMPVEC(:) = A(:,K)
		    A(:,K) = A(:,IS(K))
		    A(:,IS(K)) = TMPVEC(:)
		END DO
		
      RETURN
      END