	  ! MAT& RESULT_MAT (N,M)
	  SUBROUTINE ACT_ELU(RESULT_MAT,MAT,N,M)	  
	  INTEGER :: N,M
	  REAL(8),DIMENSION(N,M) :: MAT,RESULT_MAT	  
	  RESULT_MAT = MERGE(MAT,EXP(MAT)-1.0_8,MAT>0.0_8)
	  RETURN
	  END
	  
	  SUBROUTINE ACT_ELU_D(RESULT_MAT,MAT,N,M)	  
	  INTEGER :: N,M
	  REAL(8),DIMENSION(N,M) :: MAT,RESULT_MAT	  
	  RESULT_MAT = MERGE(1.0_8,EXP(MAT),MAT>0.0_8)
	  RETURN
	  END
	  
	  SUBROUTINE ACT_LRELU(RESULT_MAT,MAT,N,M)	  
	  INTEGER :: N,M
