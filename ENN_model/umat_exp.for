     3 WZ_2,WSZ_1,BSZ_1,
     4 WZ_3,WSZ_2,BSZ_2, 
     5 WZ_4,WSZ_3,BSZ_3,  
     6 WQ_1,BQ_1,  
     7 WQ_2,BQ_2,  
     8 WQ_3,BQ_3,  
     9 WQ_4,BQ_4)
	  
	  INCLUDE 'ABA_PARAM.INC'
	  
	  REAL(8),PARAMETER :: EPS = 1.0d-6
	  
	  REAL(8),DIMENSION(1,1) :: F
	  REAL(8),DIMENSION(4,1) :: F_S
	  
	  REAL(8),DIMENSION(9,1) :: F_D
	  
	  REAL(8),DIMENSION(4,4) :: F_SS
	  
	  REAL(8),DIMENSION(4,1) :: Svar1,Svar2
	  REAL(8),DIMENSION(4,1) :: F_Svar1,F_Svar2
	  REAL(8),DIMENSION(4,1) :: Stmp
	  
	  REAL(8),DIMENSION(4,1) :: S,EP
	  REAL(8),DIMENSION(5,1) :: Q
	  
	  REAL(8),DIMENSION(9,1) :: SY_D
	  
	  REAL(8),DIMENSION(1,1) :: SEQ,SY
	  REAL(8),DIMENSION(4,1) :: SEQ_S 
	  
	  REAL(8),DIMENSION(30,4) :: WZ_1,WSZ_1,WSZ_2
	  REAL(8),DIMENSION(30,1) :: BZ_1,BSZ_1,BSZ_2,WZ_4
	  REAL(8),DIMENSION(30, 30):: WZ_2,WZ_3
	  REAL(8),DIMENSION(1,4) :: WSZ_3
	  REAL(8),DIMENSION(1,1) :: BSZ_3
	  REAL(8),DIMENSION(30,9) :: WQ_1
	  REAL(8),DIMENSION(30,30) :: WQ_2,WQ_3
	  REAL(8),DIMENSION(30,1) :: BQ_1,BQ_2,BQ_3
	  REAL(8),DIMENSION(1,30) :: WQ_4
	  REAL(8),DIMENSION(1,1) :: BQ_4
	  
