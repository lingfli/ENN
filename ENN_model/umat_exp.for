	  RESULT_MAT = MERGE(MAT,0.01_8*MAT,MAT>0.0_8)
	  RETURN
	  END
	  
	  SUBROUTINE ACT_LRELU_D(RESULT_MAT,MAT,N,M)
	  INTEGER :: N,M
	  REAL(8),DIMENSION(N,M) :: MAT,RESULT_MAT
	  RESULT_MAT = MERGE(1.0_8,0.01_8,MAT>0.0_8)
