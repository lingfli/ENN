	  CHARACTER*80,DIMENSION(100) :: CHARV
	  CHARACTER*200 :: STR

	  CALL COMPUTE_STRESS_EQ(SEQ,SEQ_S,S, 
     1 WZ_1,BZ_1,  
     2 WZ_2,WSZ_1,BSZ_1,  
     3 WZ_3,WSZ_2,BSZ_2,  
     4 WZ_4,WSZ_3,BSZ_3)
	  
	  CALL COMPUTE_STRESS_Y(SY,SY_D, EP,Q, 
	1 WQ_1,BQ_1,  
	2 WQ_2,BQ_2,  
	3 WQ_3,BQ_3,  
	4 WQ_4,BQ_4)
	  
	  F = SEQ-SY
	  F_S = SEQ_S
	  F_D = -SY_D
	  
	  REALV(1) = F(1,1)
	  REALV(2) = SEQ(1,1)
	  REALV(3) = SY(1,1)
	  
	  !CALL STDB_ABQERR(1,"F=Seq-Sy, %R %R %R",INTV,REALV,CHARV)
	  
	  !WRITE(*,*) "Fyield",F,SEQ,SY
	  !WRITE(*,*) "EP:"
	  !WRITE(*,*) "Q:",Q
	  !WRITE(*,*) "S:",S
	  
	  DO i=1,4
		  Svar1 = 0.0_8
		  Svar2 = 0.0_8
		  Svar1(i,1) = EPS
		  Svar2(i,1) = -EPS
		  
		  CALL COMPUTE_STRESS_EQ(Stmp,F_Svar1,S+Svar1, 
	1 WZ_1,BZ_1,  
	2 WZ_2,WSZ_1,BSZ_1,  
	3 WZ_3,WSZ_2,BSZ_2,  
	4 WZ_4,WSZ_3,BSZ_3)
		  CALL COMPUTE_STRESS_EQ(Stmp,F_Svar2,S+Svar2, 
	1 WZ_1,BZ_1,  
	2 WZ_2,WSZ_1,BSZ_1, 
	3 WZ_3,WSZ_2,BSZ_2, 
	4 WZ_4,WSZ_3,BSZ_3)
		  
		  F_SS(:,i) = (F_Svar1(:,1)-F_Svar2(:,1))/(2.0_8*EPS)
		  
	  END DO
	  
	  RETURN
	  END
	  
	  
	  
